// keywordMap provides O(1) keyword lookup by mapping keyword names
// to their index in the keywords array. Built at init time.
var keywordMap map[string]int

func init() {
	keywordMap = make(map[string]int, 510)
	for i := range keywords {
		keywordMap[keywords[i].Name] = i
	}
}

// LookupKeyword returns the keyword entry and true if name is a keyword,
// or a zero Keyword and false otherwise. name must be lowercase.
func LookupKeyword(name string) (Keyword, bool) {
	if idx, ok := keywordMap[name]; ok {
		return keywords[idx], true
	}
	return Keyword{}, false
}
