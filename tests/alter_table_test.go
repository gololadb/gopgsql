func TestAlterTableColumnExtended(t *testing.T) {
	tests := []struct {
		sql     string
		desc    string
		subtype parser.AlterTableType
	}{
		{"ALTER TABLE t ALTER COLUMN c SET STATISTICS 100", "set statistics", parser.AT_SetStatistics},
		{"ALTER TABLE t ALTER COLUMN c SET STORAGE PLAIN", "set storage", parser.AT_SetStorage},
		{"ALTER TABLE t ALTER COLUMN c SET COMPRESSION lz4", "set compression", parser.AT_SetCompression},
		{"ALTER TABLE t ALTER COLUMN c ADD GENERATED ALWAYS AS IDENTITY", "add identity always", parser.AT_AddIdentity},
		{"ALTER TABLE t ALTER COLUMN c ADD GENERATED BY DEFAULT AS IDENTITY", "add identity by default", parser.AT_AddIdentity},
		{"ALTER TABLE t ALTER COLUMN c DROP IDENTITY", "drop identity", parser.AT_DropIdentity},
		{"ALTER TABLE t ALTER COLUMN c DROP IDENTITY IF EXISTS", "drop identity if exists", parser.AT_DropIdentity},
		{"ALTER TABLE t ALTER COLUMN c SET EXPRESSION AS (a + b)", "set expression", parser.AT_SetExpression},
		{"ALTER TABLE t ALTER COLUMN c DROP EXPRESSION", "drop expression", parser.AT_DropExpression},
		{"ALTER TABLE t ALTER COLUMN c DROP EXPRESSION IF EXISTS", "drop expression if exists", parser.AT_DropExpression},
	}
	for _, tt := range tests {
		t.Run(tt.desc, func(t *testing.T) {
			stmt := parseOne(t, tt.sql)
			at := stmt.(*parser.AlterTableStmt)
			if len(at.Cmds) != 1 {
				t.Fatalf("expected 1 cmd, got %d", len(at.Cmds))
			}
			if at.Cmds[0].Subtype != tt.subtype {
				t.Fatalf("expected subtype %d, got %d", tt.subtype, at.Cmds[0].Subtype)
			}
		})
	}
}


