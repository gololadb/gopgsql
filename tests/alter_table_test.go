func TestAlterTableAddColumn(t *testing.T) {
	s := parseOne(t, "ALTER TABLE t ADD COLUMN name text NOT NULL")
	alt, ok := s.(*parser.AlterTableStmt)
	if !ok {
		t.Fatalf("expected parser.AlterTableStmt, got %T", s)
	}
	if len(alt.Cmds) != 1 {
		t.Fatalf("expected 1 cmd, got %d", len(alt.Cmds))
	}
	cmd := alt.Cmds[0]
	if cmd.Subtype != parser.AT_AddColumn {
		t.Errorf("expected parser.AT_AddColumn, got %d", cmd.Subtype)
	}
	cd := cmd.Def.(*parser.ColumnDef)
	if cd.Colname != "name" {
		t.Errorf("expected name, got %s", cd.Colname)
	}
}


